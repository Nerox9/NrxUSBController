void initControllers()
{
	uint8_t inittemp[18];
	volatile uint32_t adc0Value = HAL_ADC_GetValue(&hadc2);
	volatile uint32_t adc1Value = HAL_ADC_GetValue(&hadc1);

	if (!HAL_GPIO_ReadPin(button0_GPIO_Port, button0_Pin))
		inittemp[0] = 0x10;
	else
		inittemp[0] = 0x11;

	if (!HAL_GPIO_ReadPin(button1_GPIO_Port, button1_Pin))
		inittemp[1] = 0x12;
	else
		inittemp[1] = 0x13;

	if (!HAL_GPIO_ReadPin(button2_GPIO_Port, button2_Pin))
		inittemp[2] = 0x14;
	else
		inittemp[2] = 0x15;

	if (!HAL_GPIO_ReadPin(button3_GPIO_Port, button3_Pin))
		inittemp[3] = 0x16;
	else
		inittemp[3] = 0x17;

	if (!HAL_GPIO_ReadPin(button4_GPIO_Port, button4_Pin))
		inittemp[4] = 0x18;
	else
		inittemp[4] = 0x19;

	if (!HAL_GPIO_ReadPin(button5_GPIO_Port, button5_Pin))
		inittemp[5] = 0x1A;
	else
		inittemp[5] = 0x1B;

	if (!HAL_GPIO_ReadPin(switch0_GPIO_Port, switch0_Pin))
		inittemp[6] = 0x1C;
	else
		inittemp[6] = 0x1D;

	if (!HAL_GPIO_ReadPin(switch1_GPIO_Port, switch1_Pin))
		inittemp[7] = 0x1E;
	else
		inittemp[7] = 0x1F;

	inittemp[8] = 0xF0; //pot0
	inittemp[9] = (adc1Value & 0x000000ff);
	inittemp[10] = (adc1Value & 0x0000ff00) >> 8;
	inittemp[11] = (adc1Value & 0x00ff0000) >> 16;
	inittemp[12] = (adc1Value & 0xff000000) >> 24;

	inittemp[13] = 0xF1; //pot1
	inittemp[14] = (adc0Value & 0x000000ff);
	inittemp[15] = (adc0Value & 0x0000ff00) >> 8;
	inittemp[16] = (adc0Value & 0x00ff0000) >> 16;
	inittemp[17] = (adc0Value & 0xff000000) >> 24;

	USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, &inittemp[0], 18);
}
